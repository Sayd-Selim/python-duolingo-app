                  const updatedLessons = completedLessons.filter(id => id !== 2);
                  localStorage.setItem('completedLessons', JSON.stringify(updatedLessons));
                  console.log('–£—Ä–æ–∫ —É–¥–∞–ª–µ–Ω –∏–∑ localStorage –¥–ª—è –ø–æ–≤—Ç–æ—Ä–Ω–æ–≥–æ –ø—Ä–æ—Ö–æ–∂–¥–µ–Ω–∏—è');
